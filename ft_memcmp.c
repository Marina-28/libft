#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				k;
	unsigned char	*a;
	unsigned char	*b;
	size_t			i;

	i = 0;
	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	while (i < n)
	{
		if (a[i] != b[i])
		{
			k = a[i] - b[i];
			return (k);
		}
		else
			i++;
	}
	return (0);
}
