#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	h;
	int		d;

	d = 1000000000;
	if (n < 0)
	{
		write(fd, "-", 1);
		if (n == -2147483648)
		{
			write(fd, "2", 1);
			n = -147483648;
		}
		n = n * (-1);
	}
	while (d != 1)
	{
		if ((n / d) > 0)
		{
			h = (n / d) % 10 + 48;
			write(fd, &h, 1);
		}
		d = d / 10;
	}
	h = n % 10 + 48;
	write(fd, &h, 1);
}
